import { z } from "zod";

/**
 * Zod Schema للتحقق من صحة نموذج التواصل
 */
export const contactSchema = z.object({
  name: z
    .string()
    .min(1, "الاسم مطلوب")
    .min(2, "يجب أن يكون الاسم حرفين على الأقل")
    .max(50, "يجب أن يكون الاسم 50 حرف على الأكثر"),

  email: z.string().min(1, "البريد الإلكتروني مطلوب").email("البريد الإلكتروني غير صحيح"),

  phone: z
    .string()
    .min(1, "رقم الهاتف مطلوب")
    .regex(/^(05|5)(5|0|3|6|4|9|1|8|7)([0-9]{7})$/, "رقم الهاتف غير صحيح"),

  subject: z
    .string()
    .min(1, "الموضوع مطلوب")
    .min(3, "يجب أن يكون الموضوع 3 أحرف على الأقل")
    .max(100, "يجب أن يكون الموضوع 100 حرف على الأكثر"),

  message: z
    .string()
    .min(1, "الرسالة مطلوبة")
    .min(10, "يجب أن تكون الرسالة 10 أحرف على الأقل")
    .max(1000, "يجب أن تكون الرسالة 1000 حرف على الأكثر"),
});

/**
 * نوع البيانات المُستخرج من الـ schema
 */
export type ContactFormData = z.infer<typeof contactSchema>;
